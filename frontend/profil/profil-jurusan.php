                        <div class="mu-latest-course-single">
                          <h2><a href="#">Profil Jurusan</a></h2>
                          <hr align="left">
                          <!-- <hr width="50%;" color="black" size="50" align="left">  -->
                          <figure class="mu-latest-course-img">  
                            <a href="#"><img src="../assets/img/banner.jpg" alt="img"></a>
